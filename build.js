// Build app cards — compact letter-badge layout, Figma 2026
// Per-card icon backgrounds are NOT inline styles. They're written to
// dist/card-styles.css (loaded via <link>) so a malformed iconBg slipping
// past the validator (regex regression, e.g.) cannot end up as a
// style="..." attribute. The CSS file is `'self'` per CSP.
// Dual selector so the same generated rule paints (a) the icon inside an
// .app-card on the storefront grid AND (b) the .app-icon on the per-app
// detail page hero. Detail-page templates set data-id directly on the icon
// element; storefront cards keep data-id on the wrapping card.
const cardIconBackgrounds = apps
  .map(app => {
    const safeId = escapeAttrCss(app.id);
    const bg = app.iconBg || '#2563eb';
    return `.app-card[data-id="${safeId}"] .app-icon, .app-icon[data-id="${safeId}"] { background: ${bg}; }`;
  })
  .join('\n');
function escapeAttrCss(s) {
  // The validator already constrains id to [a-z0-9-]+ so this is belt-and-suspenders.
  return String(s).replace(/[^a-z0-9_-]/gi, '_');
}

function renderAppCard(app) {
  const q = qualityScores[app.id];
  const qualityBadge = q && q.grade
    ? `<a href="/quality/${escapeHtml(app.id)}/" class="quality-badge grade-${q.grade.toLowerCase()}" title="Code quality: ${q.score}/100">${q.grade}</a>`
    : '';
  // Letter fallback lives on a data-attribute; storefront.js binds the
  // img.error listener and reads it. No more JS-string-in-HTML-attribute splicing.
  const letter = escapeHtml((app.name || '?').trim().charAt(0).toUpperCase());
  const authAttr = app.requiresAuth ? ' data-requires-auth="1"' : '';
  const iconEmoji = app.icon || `&#${(app.name || '?').codePointAt(0)};`;
  return `        <div class="app-card compact" data-id="${escapeHtml(app.id)}" data-category="${escapeHtml(app.category)}" data-about="/apps/${escapeHtml(app.id)}"${authAttr}>
          <div class="app-icon" data-letter="${letter}">
            <span class="app-emoji" aria-hidden="true">${iconEmoji}</span>
          </div>
          <div class="app-body">
            <span class="app-name">${escapeHtml(app.name)}${qualityBadge}</span>
            <span class="app-meta">${escapeHtml(categoryLabel(app.category))}</span>
          </div>
          <a href="${escapeHtml(app.appUrl)}" target="_blank" rel="noopener" class="app-cta" aria-label="Open ${escapeHtml(app.name)}">
            <svg viewBox="0 0 24 24" aria-hidden="true"><polygon points="6,4 20,12 6,20" /></svg>
            <span class="cta-label">Open</span>
          </a>
        </div>`;
}

